    private void akcijaPosaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaPosaljiActionPerformed
       
        
         // Sender's email ID needs to be mentioned
     String from = "nekretnine121@gmail.com";
     String pass ="edunovaj14";
    // Recipient's email ID needs to be mentioned.

   String host = "smtp.gmail.com";

   // Get system properties
   Properties properties = System.getProperties();
   // Setup mail server
   properties.put("mail.smtp.starttls.enable", "true");
   properties.put("mail.smtp.host", host);
   properties.put("mail.smtp.user", from);
   properties.put("mail.smtp.password", pass);
   properties.put("mail.smtp.port", "587");
   properties.put("mail.smtp.auth", "true");

   // Get the default Session object.
   Session session = Session.getDefaultInstance(properties);

   try{
      // Create a default MimeMessage object.
      MimeMessage message = new MimeMessage(session);

      // Set From: header field of the header.
      message.setFrom(new InternetAddress(from));

      // Set To: header field of the header.
       for (Polaznik p : grupa.getPolaznici()) {
           if(p.getEmail()==null || p.getEmail().trim().length()==0){
               continue;
           }
      message.addRecipient(Message.RecipientType.TO,
                               new InternetAddress(p.getEmail()));     
       }
      

      // Set Subject: header field
      message.setSubject(naslov.getText());

      // Now set the actual message
      message.setText(editor.getText(), "utf-8", "html");

      // Send message
      Transport transport = session.getTransport("smtp");
      transport.connect(host, from, pass);
      transport.sendMessage(message, message.getAllRecipients());
      transport.close();
      System.out.println("Sent message successfully....");
   }catch (MessagingException mex) {
      mex.printStackTrace();
   }
       dispose();
    }//GEN-LAST:event_akcijaPosaljiActionPerformed

   

