    private void akcijaDodajNoviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaDodajNoviActionPerformed
        reset();

        if (naziv.getText().trim().isEmpty()) {
            greska(naziv, "Obavezno naziv");
            return;
        }
        
        try {
            Integer.parseInt(postanskiBroj.getText().trim());
        } catch (NumberFormatException e) {
            greska(postanskiBroj, "Broj nije dobrog formata");
            return;
        }

        Mjesto mj = new Mjesto();

        mj.setNaziv(naziv.getText().trim());
        mj.setPostanskiBroj(postanskiBroj.getText().trim());
        mj.setDrzava(drzava.getText().trim());

        mj = obradaMjesto.dodaj(mj);

        DefaultListModel<Mjesto> m = (DefaultListModel<Mjesto>) lista.getModel();
        m.addElement(mj);
    }//GEN-LAST:event_akcijaDodajNoviActionPerformed

    private void akcijaPromjeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaPromjeniActionPerformed
        if (odabranoMjesto == null) {
            return;
        }
        
        reset();

        if (naziv.getText().trim().isEmpty()) {
            greska(naziv, "Obavezno naziv");
            return;
        }
        
        try {
            Integer.parseInt(postanskiBroj.getText().trim());
        } catch (NumberFormatException e) {
            greska(postanskiBroj, "Broj nije dobrog formata");
            return;
        }
        
        odabranoMjesto.setNaziv(naziv.getText());
        odabranoMjesto.setPostanskiBroj(postanskiBroj.getText());
        odabranoMjesto.setDrzava(drzava.getText());

        if (!obradaMjesto.promjeni(odabranoMjesto)) {
            return;
        }

        lista.repaint();
    }//GEN-LAST:event_akcijaPromjeniActionPerformed

    private void akcijaObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaObrisiActionPerformed
        odabranoMjesto = (Mjesto) lista.getSelectedValue();
        if (odabranoMjesto == null) {
            return;
        }

        if (JOptionPane.showConfirmDialog(getParent(), "Jeste li sigurni da želite obrisati: " + odabranoMjesto.getNaziv() + "?", "Brisanje mjesta", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.NO_OPTION) {
            return;
        }

        if (!obradaMjesto.obrisi(odabranoMjesto)) {
            return;
        }

        DefaultListModel<Mjesto> m = (DefaultListModel<Mjesto>) lista.getModel();
        m.removeElement(odabranoMjesto);
        lista.repaint();
        cistiPolja();
    }//GEN-LAST:event_akcijaObrisiActionPerformed

    private void listaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaValueChanged
        reset();
        odabranoMjesto = (Mjesto) lista.getSelectedValue();
        if (odabranoMjesto == null) {
            return;
        }
        naziv.setText(odabranoMjesto.getNaziv());
        postanskiBroj.setText(odabranoMjesto.getPostanskiBroj());
        drzava.setText(odabranoMjesto.getDrzava());
    }//GEN-LAST:event_listaValueChanged

    private void akcijaTraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaTraziActionPerformed

        cistiPolja();

        List<Mjesto> entiteti = obradaMjesto.dohvati(uvjet.getText());

        DefaultListModel<Mjesto> m = new DefaultListModel<>();
        for (Mjesto mj : entiteti) {
            m.addElement(mj);
        }
        lista.setModel(m);
    }//GEN-LAST:event_akcijaTraziActionPerformed

    private void cistiPolja() {
        naziv.setText("");
        postanskiBroj.setText("");
        drzava.setText("");
    }

    private void reset() {

        for (Component c : this.getContentPane().getComponents()) {
            if (c instanceof JTextField || c instanceof JPasswordField) {
                JComponent jc = (JComponent) c;
                jc.setBackground(Color.white);
                jc.setToolTipText("");
            }
        }
    }

    private void greska(JComponent komponenta, String obavijest) {
        komponenta.setBackground(Color.red);
        komponenta.requestFocus();
        komponenta.setToolTipText(obavijest);

    }
    

