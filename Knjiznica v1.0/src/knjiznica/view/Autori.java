    private void akcijaDodajNoviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaDodajNoviActionPerformed
        reset();

        if (ime.getText().trim().isEmpty()) {
            greska(ime, "Obavezno ime");
            return;
        }
        if (prezime.getText().trim().isEmpty()) {
            greska(prezime, "Obavezno prezime");
            return;
        }

        Autor a = new Autor();
        
        a.setIme(ime.getText().trim());
        a.setPrezime(prezime.getText().trim());
        a.setDatumrodenja(datum.getDateEditor().getDate());

        a = obradaAutor.dodaj(a);

        DefaultListModel<Autor> m = (DefaultListModel<Autor>) lista.getModel();
        m.addElement(a);
    }//GEN-LAST:event_akcijaDodajNoviActionPerformed

    private void akcijaPromjeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaPromjeniActionPerformed
        if (odabranoAutor == null) {
            return;
        }
        reset();

        if (ime.getText().trim().isEmpty()) {
            greska(ime, "Obavezno ime");
            return;
        }
        if (prezime.getText().trim().isEmpty()) {
            greska(prezime, "Obavezno prezime");
            return;
        }
        
        odabranoAutor.setIme(ime.getText());
        odabranoAutor.setPrezime(prezime.getText());
        odabranoAutor.setDatumrodenja(datum.getDateEditor().getDate());

        if (!obradaAutor.promjeni(odabranoAutor)) {
            return;
        }

        lista.repaint();
    }//GEN-LAST:event_akcijaPromjeniActionPerformed

    private void akcijaObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaObrisiActionPerformed
        odabranoAutor = (Autor) lista.getSelectedValue();
        if (odabranoAutor == null) {
            return;
        }

        if (JOptionPane.showConfirmDialog(getParent(), "Jeste li sigurni da želite obrisati: " + odabranoAutor.getImePrezime() + "?", "Brisanje autora", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.NO_OPTION) {
            return;
        }

        if (!obradaAutor.obrisi(odabranoAutor)) {
            return;
        }

        DefaultListModel<Operater> m = (DefaultListModel<Operater>) lista.getModel();
        m.removeElement(odabranoAutor);
        lista.repaint();
        cistiPolja();
    }//GEN-LAST:event_akcijaObrisiActionPerformed

    private void listaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaValueChanged
        reset();
        odabranoAutor = (Autor) lista.getSelectedValue();
        if (odabranoAutor == null) {
            return;
        }
        ime.setText(odabranoAutor.getIme());
        prezime.setText(odabranoAutor.getPrezime());
        datum.setDate(odabranoAutor.getDatumrodenja());
    }//GEN-LAST:event_listaValueChanged

    private void akcijaTraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaTraziActionPerformed

        cistiPolja();

        List<Autor> entiteti = obradaAutor.dohvati(uvjet.getText());

        DefaultListModel<Autor> m = new DefaultListModel<>();
        for (Autor a : entiteti) {
            m.addElement(a);
        }
        lista.setModel(m);
    }//GEN-LAST:event_akcijaTraziActionPerformed

    private void cistiPolja() {
        ime.setText("");
        prezime.setText("");
        datum.setCalendar(null);
    }

    private void reset() {

        for (Component c : this.getContentPane().getComponents()) {
            if (c instanceof JTextField || c instanceof JPasswordField) {
                JComponent jc = (JComponent) c;
                jc.setBackground(Color.white);
                jc.setToolTipText("");
            }
        }
    }

    private void greska(JComponent komponenta, String obavijest) {
        komponenta.setBackground(Color.red);
        komponenta.requestFocus();
        komponenta.setToolTipText(obavijest);

    }
    

